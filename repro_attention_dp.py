    print(f" HLO dumped to: {hlo_path}")
        
    qkv_spec = P(ShardingAxisName.ATTN_DATA, ShardingAxisName.ATTN_HEAD, None)
    kv_cache_spec = P(ShardingAxisName.ATTN_DATA, None, ShardingAxisName.ATTN_HEAD, None, None)

