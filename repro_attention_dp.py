    inputs['kv_lens'] = jax.device_put(inputs['kv_lens'], NamedSharding(mesh, P(ShardingAxisName.ATTN_DATA)))
    inputs['page_indices'] = jax.device_put(inputs['page_indices'], NamedSharding(mesh, P(ShardingAxisName.ATTN_DATA)))
    inputs['cu_q_lens'] = jax.device_put(inputs['cu_q_lens'], NamedSharding(mesh, P(ShardingAxisName.ATTN_DATA)))
    inputs['distribution'] = jax.device_put(inputs['distribution'], NamedSharding(mesh, P(ShardingAxisName.ATTN_DATA)))
    
    # Warm up
    print("Warming up...")
    for _ in range(3):
        result = attention_fn(
            inputs['q'],
            inputs['k'],
            inputs['v'],
            inputs['kv_cache'],
            inputs['kv_lens'],
            inputs['page_indices'],
            inputs['cu_q_lens'],
            inputs['distribution'],
        )
        jax.block_until_ready(result)
    
    # Benchmark
    print("Benchmarking ...")
    times = []
    dump_dir = f"gs://wenxindong-vm/trace/debug/gptoss/kernel/dp{dp}"
    jax.profiler.start_trace(dump_dir)
    for i in range(10):
        start = time.time()
        result = attention_fn(
            inputs['q'],
            inputs['k'],
            inputs['v'],
            inputs['kv_cache'],
            inputs['kv_lens'],
            inputs['page_indices'],
            inputs['cu_q_lens'],
            inputs['distribution'],
        )
        jax.block_until_ready(result)
        elapsed = time.time() - start
        times.append(elapsed)
        print(f"  Run {i+1}: {elapsed*1000:.2f} ms")
    jax.profiler.stop_trace()
    avg_time = sum(times) / len(times)
    print(f"\n Average time: {avg_time*1000:.2f} ms")
    
    return result, avg_time


