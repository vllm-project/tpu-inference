def run(inputs, dump_dir, dp_size=2, num_devices=8, dp=True):
    
    print("\n" + "="*80)
    if dp: 
        print(f"Running WITH Data Parallelism (dp_size={dp_size}, num_devices={num_devices})")
    else:
        print(f"Running WITHOUT Data Parallelism (dp_size={dp_size}, num_devices={num_devices})")
    print("="*80)
    
    print(f"Input shapes:")
    for key, val in inputs.items():
        if isinstance(val, jax.Array):
            print(f"  {key}: {val.shape} {val.dtype}")
    
    devices = jax.devices()
    dp_size = 2
    if dp: 
        device_array = np.array(devices[:8]).reshape(dp_size, -1)
    else: 
        device_array = np.array(devices[:4]).reshape(1, -1)
    mesh = Mesh(
        device_array,
        axis_names=(ShardingAxisName.ATTN_DATA, ShardingAxisName.ATTN_HEAD),
    )
    print(f"Mesh: {mesh}")

    def _attention_wrapper(q, k, v, kv_cache, kv_lens, page_indices, cu_q_lens, distribution):

        attention_metadata = AttentionMetadata(
            input_positions=None, 
            block_tables=page_indices,
            seq_lens=kv_lens,
            query_start_loc=cu_q_lens,
            request_distribution=distribution,
        )
        
        batch_size, num_heads, head_dim = q.shape
        num_kv_heads = k.shape[1]
        
        # Reshape q, k, v to match _jax_attn_func expectations
        q_reshaped = q.reshape(batch_size, num_heads * head_dim)
        k_reshaped = k.reshape(batch_size, num_kv_heads * head_dim)
        v_reshaped = v.reshape(batch_size, num_kv_heads * head_dim)
        
        new_kv_cache, outputs = _jax_attn_func(
            kv_cache=kv_cache,
            q=q_reshaped,
            k=k_reshaped,
            v=v_reshaped,
            sinks=None,
            attention_metadata=attention_metadata,
            mesh=mesh,
            scale=inputs['sm_scale'],
            head_size=head_dim,
            num_heads=num_heads,
            num_kv_heads=num_kv_heads,
            q_scale=None,
            k_scale=None,
            v_scale=None,
            sliding_window=None,
        )
        
        outputs = outputs.reshape(batch_size, num_heads, head_dim)
        
        return outputs, new_kv_cache
    
    attention_fn = jax.jit(_attention_wrapper)
    
    # Lower and dump HLO
    print("\nLowering computation...")
    lowered = attention_fn.lower(
        inputs['q'],
        inputs['k'],
        inputs['v'],
        inputs['kv_cache'],
        inputs['kv_lens'],
        inputs['page_indices'],
        inputs['cu_q_lens'],
        inputs['distribution'],
    )
    
